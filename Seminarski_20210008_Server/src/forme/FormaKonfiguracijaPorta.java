    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        int port;
        try {
            port = Integer.parseInt(jTextFieldPort.getText().trim());
            
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Na mesto broja porta su upisana slova umesto cifara", "Ispravite unos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (port>=0 && port<=65535){
            try {
                konfiguracija.Konfiguracija.getInstance().setProperty("port", port + "");
                konfiguracija.Konfiguracija.getInstance().sacuvajIzmene();
                JOptionPane.showMessageDialog(this, "Parametri konfiguracije su sačuvani.","USPEH", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Greška! Parametri konfiguracije nisu sačuvani.","GREŠKA", JOptionPane.ERROR_MESSAGE);
            }
            
        }else JOptionPane.showMessageDialog(this, "Unet je nedozvoljen broj porta. Unesite vrednost koja je veća ili jednaka 0 i manja ili jednaka 65535", "Loš unos", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

